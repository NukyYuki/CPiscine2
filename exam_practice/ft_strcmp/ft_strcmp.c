int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
	{
		i++;
	}
	return (s1[i] - s2[i]);
}
#include <stdio.h>
int	main(int ac, char **av)
{
	(void)ac;
	printf("%d", ft_strcmp(av[1], av[2]));
	return (0);
}
